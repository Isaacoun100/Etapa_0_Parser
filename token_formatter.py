import re

def update_state_numbers(input_dict):
    # Función para disminuir el número de un estado 'q<number>'
    def update_state(state):
        match = re.fullmatch(r'q(\d+)', state)
        if match:
            num = int(match.group(1)) - 1  # Disminuye el número en 1
            return f'q{num}'  # Devuelve el estado actualizado
        return state  # Si no es del tipo 'q<number>', lo deja igual

    updated_dict = {}

    # Recorremos el diccionario y actualizamos los estados
    for state, transitions in input_dict.items():
        updated_state = update_state(state)  # Actualizamos la clave (estado)
        updated_transitions = {}

        # Actualizamos las transiciones dentro de cada estado
        for symbol, next_state in transitions.items():
            updated_transitions[symbol] = update_state(next_state)

        updated_dict[updated_state] = updated_transitions

    return updated_dict

